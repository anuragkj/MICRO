MOV DS, AX
MOV AX, 0000H
MOV BX, 0001H
CALL BCD2BIN
MOV BX, 000AH
CALL BCD2BIN
MOV BX, 0064H
CALL BCD2BIN
MOV BX, 03E8H
CALL BCD2BIN
INT 3
