.MODEL SMALL
.STACK 20
.CODE
START:
MOV AL, 45H
MOV BL, AL
AND AL, 0F0H
ROR AL, 4
CALL HEXASC
MOV DL, AL
MOV AH,02
INT 21H
MOV AL, BL
AND AL, 0FH
CALL HEXASC
MOV DL, AL
MOV AH, 02H
INT 21H
MOV AH, 4CH
INT 21H
HEXASC:
CMP AL, 0AH
JB NUM
ADD AL, 07
NUM:
ADD AL, 30H
RET
END START

