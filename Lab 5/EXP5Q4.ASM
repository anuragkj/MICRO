.MODEL SMALL
.STACK 100
PRINT MACRO MSG
MOV AH, 09
LEA DX, MSG
INT 21H
ENDM
.DATA
MSG1 DB 0DH, 0AH, 'Enter a string:$'
SRC DB 80
DB ?
DB 30 DUP (?)
MSG2 DB 0DH, 0AH, 'Enter a character:$'
MSG3 DB 0DH, 0AH, 'Character found$'
MSG4 DB 0DH, 0AH, 'Character not found$'
.CODE
START:
MOV AX, @DATA
MOV DS, AX
MOV ES, AX
PRINT MSG1
LEA DX, SRC
MOV AH, 0AH
INT 21H
PRINT MSG2
MOV AH, 01H
INT 21H
MOV CL, SRC+1
MOV CH, 00
LEA DI, SRC
ADD DI, 0002
CLD
REPNE SCASB
JE FOUND
PRINT MSG4
JMP EXIT
FOUND:
PRINT MSG3
EXIT:
MOV AH,4CH
INT 21H
END START
